package ru.job4j.oop;
public class Profession {
    String name;
    String surname;
    String birthday;
    String education;

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getBirthday() {
        return birthday;
    }

    public String getEducation() {
        return education;
    }


}
